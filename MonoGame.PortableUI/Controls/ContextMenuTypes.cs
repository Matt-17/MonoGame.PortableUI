namespace MonoGame.PortableUI.Controls
{
    public enum ContextMenuTypes
    {
        /// <summary>
        /// With this, the context menu will stay after touch is remove
        /// </summary>
        OpenAndClick,
        /// <summary>
        /// With this the context menu hides as soon as touch or mouse is not hold anymore
        /// </summary>
        OpenAndHold
    }
}